    private void jButton_SelectBeritaImage_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_SelectBeritaImage_ActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Select profile picture");
        fileChooser.setCurrentDirectory(new File("C:\\images"));
        FileNameExtensionFilter extentionFilter  = new FileNameExtensionFilter("Image",".png",".jpg",".jpeg");
        fileChooser.addChoosableFileFilter(extentionFilter);
        int fileState = fileChooser.showSaveDialog(null);
        if(fileState == JFileChooser.APPROVE_OPTION){
            String path = fileChooser.getSelectedFile().getAbsolutePath();
            jLabel_ImagePath.setText(path);
            imagePath = path;
            func.displayImage(205, 145, null, path, jLabel6);
        }
    }//GEN-LAST:event_jButton_SelectBeritaImage_ActionPerformed
    
    public void populateJtableWithGenres(){
        ArrayList<My_Classes.Berita> beritaList = berita.beritaList();
        String[] colNames = {"ID","Judul Berita","Author","Deskripsi"};
        Object[][] rows = new Object[beritaList.size()][colNames.length];
        for(int i = 0; i < beritaList.size(); i++){
            rows[i][0] = beritaList.get(i).getId();
            rows[i][1] = beritaList.get(i).getJBerita();
            rows[i][2] = beritaList.get(i).getAuthor();
            rows[i][3] = beritaList.get(i).getDesk();
        }
        DefaultTableModel model = new DefaultTableModel(rows,colNames);
        jTable_Genres_.setModel(model);
    }
