    private void jButton_Add_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_Add_ActionPerformed
        // TODO add your handling code here:
        String judul = jTextField_JBerita.getText();
        String autho = jTextField_AuthorID.getText();
        String desk = jTextArea_Desc.getText();
        
        if(judul.isEmpty()){
            jLabel_EmptyFname_.setVisible(true);
        } else {
            byte[] img = null;
            if(imagePath != null){
                try {
                    Path path = Paths.get(imagePath);
                    img = Files.readAllBytes(path);
                    // member.addMember(fname,lname,pnumber,email,gender,img);
                    berita.addBerita(judul, autho, desk, img);
                    populateJtableWithGenres();
                    jTextField_JBerita.setText("");
                    jTextField_AuthorID.setText("");
                    jTextArea_Desc.setText("");        
                } catch (IOException ex) {
                    Logger.getLogger(AddBeritaForm.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Pilih gambar buat member baru!", "Tidak ada terpilih.", 2);
            }
        }
    }//GEN-LAST:event_jButton_Add_ActionPerformed

